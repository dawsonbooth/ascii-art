def test_image_to_ascii():
    image = Image.open("examples/images/einstein.jpg")
    params = {
        "width": 100,
        "height": 50,
        "font": "Courier",
        "normalize": True,
        "invert": True
    }
    ascii = image_to_ascii(image, **params)
    assert ascii == einstein
